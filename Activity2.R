#Grading System
Grademo <- 72

if (Grademo >= 90 & Grademo <= 100) {
  print("Grade: A")
} else if (Grademo >= 80 & Grademo <= 89) {
  print("Grade: B")
} else if (Grademo >= 70 & Grademo <= 79) {
  print("Grade: C")
} else if (Grademo >= 60 & Grademo <= 69) {
  print("Grade: D")
} else if (Grademo < 60) {
  print("Grade: F")
}